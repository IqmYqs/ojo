                        ในบริการนี้จะได้รับการแก้ไข หรือ (ค) เนื้อหา หรือข้อมูลใดๆ ที่คุณได้รับจาก
                        หรือผ่านบริการนี้จะมีความถูกต้องแม่นยำ</p>
                    <p class="text-justify mt-4 indent-10 text-base">ojo จะไม่รับผิดชอบต่อคอนเทนท์ใดๆ
