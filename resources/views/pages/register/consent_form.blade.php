                    <p class="text-justify mt-1 indent-12 text-base">• ข้อมูลติดต่อของคุณ รวมถึงที่อยู่ หมายเลขโทรศัพท์
                        อีเมล และข้อมูลอัตลักษณ์ที่ระบุความเป็นเจ้าของลิขสิทธิ์</p>
