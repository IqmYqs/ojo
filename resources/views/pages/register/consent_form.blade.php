                        คุณยังมีสิทธิ์ใช้บริการสมาชิกได้จนกว่าจะสิ้นสุดอายุสมาชิกปัจจุบัน
                        และระบบจะไม่ต่ออายุสมาชิกหลังจากระยะเวลาสมาชิกปัจจุบันสิ้นสุดลง</p>
