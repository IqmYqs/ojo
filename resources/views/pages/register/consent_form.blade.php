                        ขอใช้ดุลยพินิจแต่เพียงฝ่ายเดียวในการสงวนสิทธิ์การเรียกเก็บค่าธรรมเนียมสำหรับสิทธิในการเข้าถึงหรือใช้ไอเท็มเสมือนจริง
