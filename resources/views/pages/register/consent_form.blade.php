                    <p class="text-justify mt-4 indent-10 text-base">
                        หากคุณเชื่อว่างานของคุณถูกคัดลอกและโพสต์ไว้บนบริการด้วยวิธีการที่เป็นการละเมิดลิขสิทธิ์
                        กรุณาส่งคำร้องแก่เราโดยใช้แบบฟอร์มที่นี่ หากคุณติดต่อเราด้วยเรื่องที่เกี่ยวกับการละเมิดลิขสิทธิ์
                        โปรดระบุถึงข้อมูลต่อไปนี้ :</p>
                    <p class="text-justify mt-1 indent-12 text-base">• ลายมือชื่ออิเล็กทรอนิกส์
                        หรือลายมือชื่อจริงของบุคคลผู้มีอำนาจกระทำการในนามของเจ้าของลิขสิทธิ์นั้นๆ</p>
