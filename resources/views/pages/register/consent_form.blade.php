                        ออกจากอุปกรณ์ของคุณแล้วก็ตามการลบบัญชี ojo หรือแอพพลิเคชั่น ojo
                        จากอุปกรณ์ของคุณไม่ถือเป็นการยุติหรือยกเลิกการเป็นสมาชิกของคุณ ทาง ojo
                        จะยังคงเรียกเก็บเงินทั้งหมดจากวิธีการชำระเงินของคุณ จนกว่าคุณจะยุติหรือยกเลิกการเป็นสมาชิกบน ojo
