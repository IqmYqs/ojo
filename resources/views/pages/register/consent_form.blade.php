                        ชะงัก หรือส่งผลกระทบทางลบต่อบริการนี้ หรือเซิร์ฟเวอร์ หรือเครือข่ายที่เชื่อมต่อกับบริการนี้</p>
                    <p class="text-justify mt-1 indent-12 text-base">• อัพโหลดไวรัส หรือโค้ดอันตราย
                        หรือไม่ว่าในกรณีอื่นใดที่อาจทำให้เกิดความสุ่มเสี่ยงด้านความปลอดภัยของบริการนี้</p>
                    <p class="text-justify mt-1 indent-12 text-base">• ปลอมแปลงหัวเว็บไซต์
                        หรือไม่ว่าในกรณีอื่นใดที่เป็นการเปลี่ยนแปลงตัวบ่งชี้ต่างๆ
                        เพื่อที่จะปิดบังแหล่งข้อมูลที่ส่งข้อมูลใดๆ ไปยังหรือผ่านบริการนี้</p>
                    <p class="text-justify mt-1 indent-12 text-base">• "เฟรม" หรือ "มิร์เรอร์"
                        ส่วนหนึ่งส่วนใดของบริการโดยไม่ได้รับการอนุญาตเป็นลายลักษณ์อักษรล่วงหน้าจาก ojo</p>
                    <p class="text-justify mt-1 indent-12 text-base">• ใช้เมต้าแท็ก (Metatag) หรือโค้ด หรืออุปกรณ์อื่นๆ
                        ที่ประกอบด้วยส่วนอ้างอิงใดๆ ถึง ojo หรือบริการนี้ (หรือเครื่องหมายการค้า ชื่อทางการค้า
                        เครื่องหมายบริการ โลโก้ หรือสโลแกนใดๆ ของ ojo) เพื่อชี้นำบุคคลใดๆ
                        ออกไปยังเว็บไซต์อื่นใดเพื่อวัตถุประสงค์ใดๆ ก็ตาม</p>
                    <p class="text-justify mt-1 indent-12 text-base">• แก้ไข ดัดแปลง ให้ช่วงสิทธิ แปล ขาย
                        เปลี่ยนแปลงทางวิศวกรรม ถอดรหัส แปลย้อนกลับ หรือไม่ว่าในกรณีอื่นใดที่เป็นการแยกส่วนใดๆ
                        ของบริการนี้ หรือเป็นต้นเหตุให้บุคคลอื่นกระทำการดังกล่าว</p>
                    <p class="text-justify mt-1 indent-12 text-base">•
                        ใช้หรือพัฒนาแอพพลิเคชั่นของบุคคลที่สามที่โต้ตอบกับบริการหรือคอนเทนท์หรือข้อมูลของสมาชิกท่านอื่นๆ
                        โดยไม่ได้รับความยินยอมเป็นลายลักษณ์อักษรล่วงหน้าจากเรา</p>
                    <p class="text-justify mt-1 indent-12 text-base">• ใช้ เข้าถึง
                        หรือเผยแพร่โปรแกรมอินเทอร์เฟซของแอพพลิเคชั่น ojo โดยไม่ได้การอนุญาตเป็นลายลักษณ์อักษร</p>
                    <p class="text-justify mt-1 indent-12 text-base">• ตรวจสอบ สแกน หรือทดสอบความไม่มั่นคงของบริการนี้
                        หรือระบบใดๆ หรือเครือข่ายของเรา</p>
                    <p class="text-justify mt-1 indent-12 text-base">• สนับสนุน หรือส่งเสริมกิจกรรมใดๆ
                        ที่ฝ่าฝืนข้อตกลงนี้</p>
                    <p class="text-justify mt-1 indent-12 text-base">• แสดงออก หรือบอกเป็นนัยว่าถ้อยความใดๆ
                        ที่คุณแถลงได้รับความเห็นชอบจาก ojo</p>
                    <p class="text-justify mt-1 indent-10 text-base">ojo อาจสืบสวนและดำเนินคดีทางกฎหมายใดๆ
                        หากมีการใช้บริการอย่างผิดกฎหมาย หรือโดยไม่ได้รับอนุญาต ซึ่งรวมถึงยกเลิกบัญชีของคุณด้วย
                        ซอฟต์แวร์ใดๆ ที่เราจัดหาไว้ให้คุณอาจจะดาวน์โหลดและติดตั้งการอัพเกรด อัพเดท หรือฟีเจอร์ใหม่ๆ
                        เองโดยอัตโนมัติ คุณมีสิทธิแก้ไขการดาวน์โหลดอัตโนมัติเหล่านี้ได้ผ่านการตั้งค่าในอุปกรณ์ของคุณ</p>

                    <h3 class="text-justify mt-4 text-lg font-bold">7. สิทธิที่คุณมอบให้ ojo</h3>

                    <p class="text-justify mt-4 indent-10 text-base">ในการสร้างบัญชีของคุณหมายความว่า
                        คุณมอบสิทธิและไลเซนส์ให้ ojo ในลักษณะที่ครอบคลุมไปทั่วโลก ถ่ายโอนได้ ให้ใช้สิทธิช่วงได้
                        ปราศจากค่าธรรมเนียมสิทธิ ที่จะโฮสต์ จัดเก็บ ใช้งาน ทำสำเนา แสดง ทำซ้ำ เปลี่ยนแปลง แก้ไข เผยแพร่
                        ดัดแปลง และแจกจ่ายข้อมูลที่คุณอนุญาตให้เราเข้าถึงจากบุคคลที่สาม เช่น จาก Facebook
                        และรวมถึงข้อมูลที่คุณโพสท์ อัพโหลด แสดง หรือทำให้เข้าถึงได้ในบริการ (เรียกรวมว่า "โพสท์")
                        หรือส่งต่อให้กับสมาชิกท่านอื่นๆ (เรียกรวมว่า "คอนเทนท์") ไลเซนส์ของ ojo
                        สำหรับคอนเทนท์ของคุณนั้นเป็นแบบไม่ผูกขาด แต่ไลเซนส์ของ ojo
                        จะเป็นแบบผูกขาดในแง่ของงานที่ถูกสร้างขึ้นผ่านการใช้บริการ ตัวอย่างเช่น ojo
                        จะมีสิทธิผูกขาดสำหรับสกรีนช็อตที่มีคอนเทนท์ของคุณอยู่</p>
                    <p class="text-justify mt-4 indent-10 text-base">นอกจากนี้เพื่อให้ ojo
                        สามารถป้องกันการใช้คอนเทนท์ของคุณนอกบริการ คุณอนุญาตให้ ojo
                        ดำเนินการในนามของคุณในแง่ของการละเมิดการใช้งานคอนเทนท์ของคุณจากบริการโดยสมาชิกท่านอื่นหรือบุคคลที่สามได้
                        ไลเซนส์ของเราในการใช้คอนเทนท์ของคุณถือเป็นสิทธิของคุณภายใต้กฎหมายที่บังคับใช้ (ตัวอย่างเช่น
                        กฎหมายที่เกี่ยวกับการคุ้มครองข้อมูลส่วนบุคคลตามขอบเขตคอนเทนท์ที่มีข้อมูลส่วนบุคคลปรากฏ
                        ตามที่กำหนดไว้ในกฎหมายเหล่านั้น) และเพื่อวัตถุประสงค์เฉพาะในการดำเนินงาน การสร้าง การจัดเสนอ
                        และการปรับปรุงบริการ รวมถึงการวิจัยและพัฒนาใหม่ๆ
                        คุณยอมรับว่าสมาชิกท่านอื่นและบุคคลอื่นที่เข้าชมหรือเข้าร่วมในบริการ (เช่น
                        บุคคลอื่นที่อาจได้รับคอนเทนท์ที่แชร์มาจากสมาชิก ojo คนอื่นๆ) สามารถดูคอนเทนท์ใดๆ
                        ที่คุณเสนอหรือที่คุณอนุญาตให้เราเสนอในบริการได้
                        คุณยอมรับว่าข้อมูลทั้งหมดที่คุณส่งมอบระหว่างการสร้างบัญชีของคุณ
                        รวมถึงข้อมูลที่มีการส่งมอบจากบัญชี ojo ของคุณ เป็นข้อมูลที่ถูกต้องและเป็นความจริง
                        และคุณมีสิทธิในการโพสท์เนื้อหาในบริการนี้ และมอบสิทธิอนุญาตให้แก่ ojo ดังที่กล่าวไว้ข้างต้น
                        คุณเข้าใจและยอมรับว่าเราอาจจะติดตาม หรือตรวจสอบเนื้อหาใดๆ ที่คุณโพสท์
                        ซึ่งถือเป็นส่วนหนึ่งของบริการนี้ เราอาจทำการลบเนื้อหาใดๆ ไม่ว่าจะทั้งหมดหรือบางส่วน
                        โดยขึ้นกับวิจารณญาณของเราแต่เพียงผู้เดียวว่ามีการละเมิดสัญญานี้
                        หรืออาจมีการทำให้เสื่อมเสียชื่อเสียงของบริการนี้ เมื่อต้องสื่อสารกับตัวแทนฝ่ายดูแลลูกค้าของเรา
                        คุณยอมรับที่จะให้ความเคารพและแสดงออกอย่างนุ่มนวล
                        หากเรารู้สึกว่าพฤติกรรมของคุณที่มีต่อตัวแทนฝ่ายดูแลลูกค้าคนใดของเราหรือพนักงานของเราเป็นการคุกคาม
                        ก่อกวน หรือก้าวร้าว ไม่ว่าในเวลาไหนก็ตาม เราขอสงวนสิทธิ์ในยกเลิกบัญชีของคุณโดยทันที เนื่องจาก
                        ojo อนุญาตให้คุณใช้บริการนี้ คุณยอมรับว่าเรา บริษัทในเครือ
                        และพันธมิตรจากบริษัทภายนอกอาจลงโฆษณาในบริการนี้ได้ โดยการส่งคำแนะนำหรือข้อติชมให้กับ ojo
                        ในเรื่องเกี่ยวกับบริการของเรา คุณยอมรับว่า ojo
                        อาจใช้หรือแชร์คำแนะนำดังกล่าวไม่ว่าจะด้วยวัตถุประสงค์ใดก็ตาม โดยไม่ต้องจ่ายค่าตอบแทนแก่คุณ</p>
                    <p class="text-justify mt-4 indent-10 text-base">โปรดทราบว่าในการทำข้อตกลงนี้กับคุณ ojo อาจเข้าถึง
                        จัดเก็บ และเปิดเผยข้อมูลบัญชีและคอนเทนท์ของคุณตามที่กฎหมายกำหนด
                        หรือกระทำด้วยความเชื่อโดยสุจริตว่า การเข้าถึง จัดเก็บ
                        หรือเปิดเผยดังกล่าวเป็นการกระทำที่ชอบด้วยกฎหมาย รวมทั้งเป็น (1) การปฏิบัติตามกระบวนการทางกฎหมาย
                        (2) การบังคับใช้ตามข้อตกลงฉบับนี้ (3)
                        การกระทำตามข้อร้องเรียนว่ามีคอนเทนท์ที่ละเมิดสิทธิของบุคคลที่สาม (4)
                        การกระทำตามคำร้องขอของคุณว่าด้วยการบริการลูกค้า หรือ (5) การปกป้องสิทธิ ทรัพย์สิน
                        หรือความปลอดภัยส่วนบุคคลของบริษัทฯ หรือบุคคลอื่น</p>

                    <h3 class="text-justify mt-4 text-lg font-bold">8. กฎของชุมชน</h3>

                    <p class="text-justify mt-4 indent-10 text-base">การตกลงใช้บริการนี้หมายความว่าคุณยินยอมที่จะไม่ :
                    </p>
                    <p class="text-justify mt-1 indent-12 text-base">• ใช้บริการนี้โดยมีวัตถุประสงค์ใดๆ ที่ขัดต่อกฎหมาย
                        หรือเป็นข้อห้ามของข้อตกลงนี้</p>
                    <p class="text-justify mt-1 indent-12 text-base">•
                        ใช้บริการเพื่อวัตถุประสงค์ที่เป็นอันตรายหรือชั่วร้ายใดๆ</p>
                    <p class="text-justify mt-1 indent-12 text-base">• ละเมิด หลักเกณฑ์ของชุมชน ของเรา
                        ซึ่งได้รับการปรับปรุงเป็นครั้งคราว</p>
                    <p class="text-justify mt-1 indent-12 text-base">• สร้างสแปม เรียกร้องเงิน หรือล่อลวงสมาชิกท่านอื่น
                    </p>
                    <p class="text-justify mt-1 indent-12 text-base">• ปลอมตัวเป็นบุคคล หรือองค์กรใดๆ หรือโพสท์รูปภาพใดๆ
                        ของบุคคลอื่นโดยไม่ได้รับอนุญาตจากบุคคลนั้น</p>
                    <p class="text-justify mt-1 indent-12 text-base">• กลั่นแกล้ง "ไล่ตาม" ข่มขู่ ทำร้ายร่างกาย คุกคาม
                        ทารุณ หรือหมิ่นประมาทบุคคลใดๆ</p>
                    <p class="text-justify mt-1 indent-12 text-base">•
                        โพสท์คอนเทนท์ที่ฝ่าฝืนหรือละเมิดสิทธิของผู้ใดผู้หนึ่ง รวมถึงสิทธิของการเผยแพร่ประชาสัมพันธ์
                        ความเป็นส่วนตัว ลิขสิทธิ์ เครื่องหมายการค้า หรือทรัพย์สินทางปัญญาหรือสิทธิในทางสัญญาอื่นๆ</p>
                    <p class="text-justify mt-1 indent-12 text-base">• โพสท์คอนเทนท์ที่เป็นคำพูดแสดงความเกลียดชัง ข่มขู่
                        เกี่ยวกับเรื่องทางเพศอย่างโจ่งแจ้ง หรือลามกอนาจาร</p>
                    <p class="text-justify mt-1 indent-12 text-base">• โพสท์คอนเทนท์ที่ก่อให้เกิดความรุนแรง
                        หรือมีภาพเปลือย มีภาพแสดงความรุนแรงหรือรูปความรุนแรงที่ไม่จำเป็น</p>
                    <p class="text-justify mt-1 indent-12 text-base">• โพสท์เนื้อหาใดๆ
                        ที่เป็นการส่งเสริมการเหยียดเชื้อชาติ คลั่งศาสนา ความเกลียดชัง หรือการทำร้ายร่างกาย
                        ไม่ว่าจะต่อกลุ่มใด หรือบุคคลใดก็ตาม</p>
                    <p class="text-justify mt-1 indent-12 text-base">• เรียกร้องขอรหัสผ่านเพื่อวัตถุประสงค์ใดๆ
                        หรือขอข้อมูลระบุตัวตนเพื่อวัตถุประสงค์ในเชิงพาณิชย์หรือวัตถุประสงค์ที่ไม่ชอบด้วยกฎหมายจากสมาชิกท่านอื่นๆ
                        หรือเผยแพร่ข้อมูลส่วนบุคคลของบุคคลอื่น โดยไม่ได้รับอนุญาตจากบุคคลนั้น</p>
                    <p class="text-justify mt-1 indent-12 text-base">• ใช้บัญชีของสมาชิกท่านอื่น
                        แชร์บัญชีกับสมาชิกท่านอื่น หรือมีมากกว่าหนึ่งบัญชี</p>
                    <p class="text-justify mt-1 indent-12 text-base">• สร้างบัญชีอื่นหลังจากที่เราปิดบัญชีของคุณไปแล้ว
                        เว้นเสียแต่ว่าคุณได้รับการอนุญาตจากเรา</p>
                    <p class="text-justify mt-1 indent-10 text-base">ojo
                        ขอสงวนสิทธิ์ในการตรวจสอบและ/หรือยกเลิกบัญชีของคุณโดยไม่คืนเงินสำหรับการซื้อใดๆ
                        หากคุณละเมิดข้อตกลงนี้ ใช้บริการในทางที่ผิด หรือประพฤติในลักษณะที่ ojo
                        เห็นว่าไม่เหมาะสมหรือผิดกฎหมาย ซึ่งรวมถึงการกระทำหรือการสื่อสารที่เกิดขึ้นทั้งในและนอกบริการ</p>

