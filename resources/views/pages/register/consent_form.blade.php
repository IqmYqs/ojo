                        และคุณเป็นเจ้าของลิขสิทธิ์หรือผู้ได้รับอนุญาตให้กระทำการแทนเจ้าของลิขสิทธิ์</p>
                    <p class="text-justify mt-1 indent-10 text-base">ojo
                        จะดำเนินการปิดบัญชีของผู้ละเมิดลิขสิทธิ์ที่กระทำผิดซ้ำ</p>

                    <h3 class="text-justify mt-4 text-lg font-bold">12. ข้อจำกัดความรับผิดชอบ</h3>

                    <p class="text-justify mt-4 indent-10 text-base">ojo จัดหาบริการในลักษณะ “ตามที่เป็น” และ”ตามที่มี”
                        และภายใต้ขอบเขตสูงสุดของกฎหมายที่ใช้บังคับพึงอนุญาต ขอปฏิเสธการรับประกันในทุกประการ
                        ไม่ว่าโดยชัดแจ้ง โดยนัย โดยชอบด้วยกฎหมาย หรือไม่ว่าในกรณีอื่นใดที่เกี่ยวข้องกับบริการนี้
                        (รวมถึงเนื้อหาทั้งหมดที่ประกอบอยู่ในข้อตกลงนี้)
